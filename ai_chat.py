try:
    import psycopg2
    DATABASE_URL = os.getenv("DATABASE_URL")
    if DATABASE_URL:
        db_conn = psycopg2.connect(DATABASE_URL)
        db_conn.autocommit = True
        
        # Ensure the table exists
        with db_conn.cursor() as cur:
            cur.execute("""
                CREATE TABLE IF NOT EXISTS global_memory (
                    id bigint generated by default as identity primary key,
                    memory_text text not null,
                    created_at timestamp with time zone default timezone('utc'::text, now()) not null
                );
            """)
    else:
        db_conn = None
except Exception as e:
    print(f"Database connection error: {e}")
    db_conn = None

global_memory_cache = ""
memory_loaded = False

def load_global_memory():
    global global_memory_cache, memory_loaded
    if not db_conn or memory_loaded:
        return
    try:
        with db_conn.cursor() as cur:
            cur.execute("SELECT memory_text FROM global_memory ORDER BY created_at ASC;")
            rows = cur.fetchall()
            memories = [row[0] for row in rows]
            global_memory_cache = "\n".join(memories)
            memory_loaded = True
    except Exception as e:
        print(f"Failed to load global memory: {e}")

def save_global_memory(text: str):
    global global_memory_cache
    if not db_conn:
        return
    try:
        with db_conn.cursor() as cur:
            cur.execute("INSERT INTO global_memory (memory_text) VALUES (%s)", (text,))
        if global_memory_cache:
            global_memory_cache += f"\n{text}"
        else:
            global_memory_cache = text
    except Exception as e:
        print(f"Failed to save global memory: {e}")

